const QStringList &SkavenFaction::getUnits() const
{
  return m_Units;
}

QList<WarScroll> SkavenFaction::getWarScrolls() const
{
  QList<WarScroll> warScrolls;
  for (auto ws : m_NameToWarScroll.toStdMap()) {
    warScrolls.push_back(ws.second(ws.first));
  }
  return warScrolls;
}
