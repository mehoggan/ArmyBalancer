const QStringList &WarriorsOfChaosFaction::getUnits() const
{
  return m_Units;
}

QList<WarScroll> WarriorsOfChaosFaction::getWarScrolls() const
{
  QList<WarScroll> warScrolls;
  for (auto ws : m_NameToWarScroll.toStdMap()) {
    warScrolls.push_back(ws.second(ws.first));
  }
  return warScrolls;
}
